# 2 : 2-1 (1)
# 3 : 3-1 (1)
# 4 : 4-3-1 / 4-2-1 (2)
# 5 : 5-4-3-1 / 5-4-2-1 (3)
# 6 : 6-3-1 / 6-2-1 (2)
# 7 : 7-6-3-1 / 7-6-2-1 (3)

# DP (bottom-up)

N = int(input())
c = [0] * (N+1)
for i in range(2, N+1):
    c[i] = c[i-1] + 1
    if i%2==0:
        c[i] = min(c[i], c[i//2] + 1)
    if i%3==0:
        c[i] = min(c[i], c[i//3] + 1)
print(c[N])